----------------------------------------------------------------------------
--
-- Repository factory providing singleton access to repository instances
--
----------------------------------------------------------------------------

----------------------------------------------------------------------------
-- Dependencies
----------------------------------------------------------------------------
local file_repository = require("spring-initializr.dao.dal.file.file_repository")

----------------------------------------------------------------------------
-- Module state
----------------------------------------------------------------------------
local M = {
    _instance = nil,
}

----------------------------------------------------------------------------
--
-- Gets the singleton repository instance.
--
-- @return table  Repository instance
--
----------------------------------------------------------------------------
function M.get_instance()
    if not M._instance then
        M._instance = file_repository
    end

    return M._instance
end

----------------------------------------------------------------------------
-- Exports
----------------------------------------------------------------------------
return M
