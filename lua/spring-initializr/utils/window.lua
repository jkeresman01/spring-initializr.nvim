local M = {}

function M.get_winid(comp)
    return comp.winid or (comp.popup and comp.popup.winid)
end

function M.safe_close(winid)
    if winid and vim.api.nvim_win_is_valid(winid) then
        pcall(vim.api.nvim_win_close, winid, true)
    end
end

return M
